# 6-2. Favorite Numbers
# Use a dictionary to store people’s favorite numbers.
# Think of five names, and use them as keys in your dictionary. Think of
# a favorite number for each person, and store each as a value in your
# dictionary.
favorite_numbers = {
    'sarah': 4,
    'james': 7,
    'carla': 2,
    'katie': 3,
    'larry': 8,
    }

# Print each person’s name and their favorite number. For even more fun,
# poll a few friends and get some actual data for your program.
print(f"Sarah's favorite number is {favorite_numbers['sarah']}.")
print(f"James's favorite number is {favorite_numbers['james']}.")
print(f"Carla's favorite number is {favorite_numbers['carla']}.")
print(f"Katie's favorite number is {favorite_numbers['katie']}.")
print(f"Larry's favorite number is {favorite_numbers['larry']}.")
