# Checking for equality
car = 'bmw'
print(car == 'bmw')

## Checking for inequality
print(car == 'audi')

## Numerical comparisons
age = 18
print(age == 18)
print(age < 21)
print(age <= 21)
print(age > 21)
print(age >= 21)

## Checking multiple conditions
age_0 = 22
age_1 = 18
print((age_0 >= 21) and (age_1 >= 21))
print((age_0 >= 21) or (age_1 >= 21))